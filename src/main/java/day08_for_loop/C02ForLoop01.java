package day08_for_loop;

public class C02ForLoop01 {

    public static void main(String[] args) {
        //Type "Hi" 5 times on the console.

        for ( int i=0; i<6; i++ ) {
            System.out.println("Hi");
        }

        //Example:2 Type code to print numbers from 15 to 4 on the console
        for (int i =15; i>3; i--){
            System.out.print(i + " ");
        }

    }

}
